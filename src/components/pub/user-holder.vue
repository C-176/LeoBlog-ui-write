<script>

export default {
  name: "user-holder",
  props: {

    userId: {
      type: Number,
      default: null
    },
  }, data() {
    return {
      userx: {},
      follows: [],
      fans: []

    }
  },
  methods: {},
  watch: {
    userx: {
      handler: function (val, oldVal) {
        this.userx = val
      },
      deep: true
    }
  },
  beforeCreate() {
    if (this.userId != null && this.userId != undefined) {
      if (this.userId == -1) {
        this.userx = this.$store.state.user
        return
      }
      this.$store.dispatch('getUserById', this.userId).then(res => {
        this.userx = res
      })
    } else {
      this.userx = this.$store.state.user
    }

  }

}
</script>

